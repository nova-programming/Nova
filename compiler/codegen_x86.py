        r("    push offset L_ret_api_call_1")
        r("    push eax")
        r("    ret")
        r("L_ret_api_call_1:")
        r("    push 0")
        r("    push 0")
        r("    push 0")
        r("    push 0")
        r("    mov ecx, [ebp + 8]")
        r("    push ecx")
        r("    push eax")
        r("    mov eax, [pInternetOpenUrlA]")
        r("    push offset L_ret_api_call_2")
        r("    push eax")
        r("    ret")
        r("L_ret_api_call_2:")
        r("    pop ebx") # restore struct ptr
        r("    mov [ebx], eax") # save handle
        r("    push 0")
        r("    push offset temp_size")
        r("    push offset temp_status")
