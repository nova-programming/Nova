        r("    mov esp, ebp")
        r("    pop ebp")
        r("    ret")

        r("_api_get_internal:")
        r("    push ebp")
        r("    mov ebp, esp")
        r("    sub esp, 4096")
        r("    mov ebx, [ebp + 8]")
        r("    mov ecx, [ebx + 4]")
        r("    cmp ecx, 400")
        r("    jge L_api_get_error")
        r("    push 8")
        r("    call _malloc")
        r("    add esp, 4")
        r("    mov edi, eax")
        r("    mov dword ptr [edi], 0")
        r("    mov dword ptr [edi+4], 0")
        r("L_api_get_loop:")
        r("    push offset temp_size")
        r("    push 4096")
        r("    lea eax, [ebp - 4096]")
        r("    push eax")
        r("    mov eax, [ebx]")
        r("    push eax")
        r("    mov eax, [pInternetReadFile]")
        r("    push offset L_ret_api_call_4")
        r("    push eax")
        r("    ret")
        r("L_ret_api_call_4:")
